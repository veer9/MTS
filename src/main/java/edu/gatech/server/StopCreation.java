package edu.gatech.server;

class StopCreation {
  public StopCreation() {}
  private double xCoord = 0.0;
  private double yCoord = 0.0;
  private String stopName = "";

  public double getXCoord() {
    return xCoord;
  }

  public double getYCoord() {
    return yCoord;
  }

  public String getName() {
    return stopName;
  }
}
